//declaring variables using const
//cannot be redeclared or re-assigned
const schoolName = "Moringaschool"
console.log(schoolName)

//declaring and assigning variables using var keyword
//can be redeclared and reassigned
var drink = "Beer" 
var drink = "Soda" 
console.log(drink)

//declaring and assigning variables using let keyword
// cannot be redeclared but can be re-assigned
let age = 18;
age = 17
console.log(age)

